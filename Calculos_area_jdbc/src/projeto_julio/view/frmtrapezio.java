    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Trapezio s = new Trapezio();
        TrapezioDao sdao = new TrapezioDao();
        
     String campo =null;
        
        if (cbcons.getSelectedIndex()==0) {
            campo="cod_tra";
        }else if (cbcons.getSelectedIndex()==1){
            campo="a";
        }else if (cbcons.getSelectedIndex()==2){
            campo="b";
        }else if (cbcons.getSelectedIndex()==3){
            campo="c";
        }else if (cbcons.getSelectedIndex()==4){
            campo="d";
        }else if (cbcons.getSelectedIndex()==5){
            campo="p";
        }
        
        sdao.pesquisar(campo, txtcons.getText(),s);
        
        txtcons.setText(""+s.getCod_tra());
        txta.setText(""+s.getA());
        txtb.setText(""+s.getB());
        txtc.setText(""+s.getC());
        txtd.setText(""+s.getD());
        txtp.setText(""+s.getP());
        
          List<Trapezio> para = sdao.getLista(campo , txtcons.getText());
        DefaultTableModel model = (DefaultTableModel) tblpar.getModel();
        model.setNumRows(0);
        
        for (Trapezio so : para){
            model.addRow(new Object[]{
                so.getCod_tra(),
                so.getA(),
                so.getB(),
                so.getC(),
                so.getD(),
                so.getP(),
                
            });
        }
        if(txta.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Cliente não encontrado");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

