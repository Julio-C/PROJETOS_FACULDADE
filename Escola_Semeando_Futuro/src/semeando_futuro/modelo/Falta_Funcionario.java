

package semeando_futuro.modelo;


public class Falta_Funcionario {
   Integer idFalta, idFuncionario;
   String Data_Falta, Motivo_Falta;

    public Integer getIdFalta() {
        return idFalta;
    }

    public void setIdFalta(Integer idFalta) {
        this.idFalta = idFalta;
    }

    public Integer getIdFuncionario() {
        return idFuncionario;
    }

    public void setIdFuncionario(Integer idFuncionario) {
        this.idFuncionario = idFuncionario;
    }

    public String getData_Falta() {
        return Data_Falta;
    }

    public void setData_Falta(String Data_Falta) {
        this.Data_Falta = Data_Falta;
    }

    public String getMotivo_Falta() {
        return Motivo_Falta;
    }

    public void setMotivo_Falta(String Motivo_Falta) {
        this.Motivo_Falta = Motivo_Falta;
    }
   
   
   
}
