    private void btn_cadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadActionPerformed
        Aluno a = new Aluno();

        if (txt_nome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o nome corretamente");
        } else if (txt_sobrenome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o sobrenome corretamente");
        }else if (txt_nasc.getDate()==null) {
            JOptionPane.showMessageDialog(null, "Digite a data de nascimento corretamente");
        }else if (txt_RA.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o RA do aluno corretamente");
        }else if(txt_Mensalidade.getText().equals("R$    ,  ")){
            JOptionPane.showMessageDialog(null, "Digite a Mensalidade do aluno corretamente");
        }else if (lbl_AnMATRI.getText().equals("An. Matrícula:")) {
            JOptionPane.showMessageDialog(null, "Anexe a Matrícula corretamente");
        }else if (lbl_AnNASC.getText().equals("An. Nascimento:")) {
            JOptionPane.showMessageDialog(null, "Anexe a Certidão corretamente");
        }else if (lbl_AnRES.getText().equals("An. Residência:")) {
            JOptionPane.showMessageDialog(null, "Anexe o Comprovante de Residência corretamente");
        }else if (lbl_AnVASC.getText().equals("An. Vacina:")) {
            JOptionPane.showMessageDialog(null, "Anexe a Carteira de Vacinação corretamente");
        } else {
            try {
                java.util.Date F = txt_nasc.getDate();
                SimpleDateFormat format1 = new SimpleDateFormat("yyyy/MM/dd");
                String datanasc = format1.format(F);

                a.setNome(txt_nome.getText());
                a.setSobrenome(txt_sobrenome.getText());
                a.setNascimento(datanasc);
                a.setTurma(cb_turma.getSelectedItem().toString());              
                a.setRA(txt_RA.getText());
                a.setMensalidade(Double.parseDouble(txt_Mensalidade.getText().replace(",", ".").replace("R$", "")));
                a.setRG_Mae(cb_RGMae.getSelectedItem().toString());
                a.setRG_Pai(cb_RGPai.getSelectedItem().toString());
                a.setCaminhoFoto(lbl_CaminhoFoto.getText());
                a.setCaminhoCert(lbl_caminhoNasc.getText());
                a.setCaminhoMatri(lbl_caminhomatri.getText());
                a.setCaminhoVac(lbl_caminhoVasc.getText());
                a.setCaminhoRes(lbl_caminhoRes.getText());
                
            } catch (Exception e) {

            }
            AlunoDAO adao = new AlunoDAO();
            adao.adciona(a);
            JOptionPane.showMessageDialog(null, "Aluno Cadastrado com sucesso");
        }


    }//GEN-LAST:event_btn_cadActionPerformed

