    private void btn_procActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_procActionPerformed
        String caminho = null;
        JFileChooser chooser = new JFileChooser("c:");
        String file = "c:";

        try {
            chooser.showOpenDialog(null);
            File n = chooser.getSelectedFile().getAbsoluteFile();
            caminho = n.toString();
            lbl_CaminhoFoto.setText(caminho);
            if (caminho != file) {
                try {
                    ImageIcon icon = new ImageIcon(caminho);
                    icon.setImage(icon.getImage().getScaledInstance(lblfoto.getWidth(), lblfoto.getHeight(), 1));
                    lblfoto.setIcon(icon);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro: " + e);
                }
            } else {
                chooser.cancelSelection();

            }

        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Selecione um arquivo! Erro: " + e);

        }

    }//GEN-LAST:event_btn_procActionPerformed

