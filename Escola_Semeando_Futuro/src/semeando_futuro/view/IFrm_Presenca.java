    private void btn_cadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadActionPerformed
        Presenca a = new Presenca();

        if (txt_dataCad.getDate()==null) {
            JOptionPane.showMessageDialog(null, "Selecione a Data Corretamente");
        } else if (txt_aulasCad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite a Quantidade de aulas Corretamente");
        }else if (txt_FaltasCad.getText()==null) {
            JOptionPane.showMessageDialog(null, "Digite a Quantidade de Faltas Corretamente");
        } else {
            try {
                java.util.Date F = txt_dataCad.getDate();
                SimpleDateFormat format1 = new SimpleDateFormat("yyyy/MM/dd");
                String datachamada = format1.format(F);

                a.setFuncionario(cb_FuncCad.getSelectedItem().toString());
                a.setTurma(cb_TurmaCad.getSelectedItem().toString());
                a.setRA_Aluno(cb_AlunoCad.getSelectedItem().toString());
                a.setData(datachamada);
                a.setAula(Integer.parseInt(txt_aulasCad.getText()));
                a.setFalta(Integer.parseInt(txt_FaltasCad.getText()));
                
                
            } catch (Exception e) {

            }
            PresencaDAO adao = new PresencaDAO();
            adao.adciona(a);
            JOptionPane.showMessageDialog(null, "Chamada Cadastrada com sucesso");
        }

    }//GEN-LAST:event_btn_cadActionPerformed

