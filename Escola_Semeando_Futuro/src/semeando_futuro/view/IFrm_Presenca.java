    private void btn_PesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_PesActionPerformed
        PresencaDAO ldao = new PresencaDAO();
        Presenca l = new Presenca();
        String campo=null;
        if(cb_pes.getSelectedIndex()==0){
            campo="Aluno.RA";
        }else if(cb_pes.getSelectedIndex()==1){
            campo="data_Pres";
        }else if(cb_pes.getSelectedIndex()==2){
            campo="Funcionario.Nome";
        }
        

        ldao.Consultar(campo, txt_Pes.getText(), l);
             
    
  
        
        
        cb_FuncCons.setSelectedItem("" + l.getFuncionario());
        txt_RAAluno.setText(""+l.getRA_Aluno());
        txt_dataCons.setDate(l.getDate());
        txt_AulaCons.setText(""+l.getAula());
        txt_FaltaCons.setText(""+l.getFalta());

        List<Presenca> presencas = ldao.getLista(campo, txt_Pes.getText());
        DefaultTableModel model = (DefaultTableModel) tbl_presenca.getModel();
        model.setNumRows(0);

        for (Presenca presenca : presencas) {
            model.addRow(new Object[]{
                presenca.getIdPresenca(),
                presenca.getRA_Aluno(),
                presenca.getFuncionario(),
                presenca.getDate(),
                presenca.getAula(),
                presenca.getFalta()});
        }
        
    }//GEN-LAST:event_btn_PesActionPerformed

