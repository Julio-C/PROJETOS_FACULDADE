    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        Desligamento a = new Desligamento();

        if (txt_dataCos.getDate()==null) {
            JOptionPane.showMessageDialog(null, "Selecione a Data Corretamente");
        } else if (txt_MotivoCons.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o Motivo Corretamente");
        } else {
            try {
                java.util.Date F = txt_dataCos.getDate();
                SimpleDateFormat format1 = new SimpleDateFormat("yyyy/MM/dd");
                String datachamada = format1.format(F);

                a.setRAALuno(txt_RAAluno.getText().toString());
                a.setData_Desligamento(datachamada);
                a.setMotivo(txt_MotivoCons.getText().toString());
                a.setIdDesligamento(Integer.parseInt(lbl_id.getText()));
                
                
            } catch (Exception e) {

            }
            DesligamentoDAO adao = new DesligamentoDAO();
            adao.Alterar(a);
            JOptionPane.showMessageDialog(null, "Desligamento Cadastrado com sucesso");
        }
    }//GEN-LAST:event_btn_alterarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        Desligamento p = new Desligamento();
        int r = JOptionPane.showConfirmDialog(null, "Deseja excluir esse registro");
        if (r == 0) {
            p.setIdDesligamento(Integer.parseInt(lbl_id.getText()));
            DesligamentoDAO cdao = new DesligamentoDAO();
            cdao.remove(p);
            JOptionPane.showMessageDialog(null, "Desligamento Removido com Sucesso");
            btn_pesActionPerformed(evt);
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

