    private void btn_pesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesActionPerformed
        DesligamentoDAO ldao = new DesligamentoDAO();
        Desligamento l = new Desligamento();
        String campo=null;
        if(cb_pes.getSelectedIndex()==0){
            campo="Aluno.RA";
        }else if(cb_pes.getSelectedIndex()==1){
            campo="data_Desl";
        }
        

        ldao.Consultar(campo, txt_pes.getText(), l);
             
    
  
        
        
        txt_RAAluno.setText("" + l.getRAALuno());
        lbl_id.setText(l.getIdDesligamento().toString());
        txt_dataCos.setDate(l.getDate());
        txt_MotivoCons.setText(l.getMotivo().toString());

        List<Desligamento> desligamentos = ldao.getLista(campo, txt_pes.getText());
        DefaultTableModel model = (DefaultTableModel) tbl_desligamento.getModel();
        model.setNumRows(0);

        for (Desligamento desligamento : desligamentos) {
            model.addRow(new Object[]{
                desligamento.getIdDesligamento(),
                desligamento.getRAALuno(),
                desligamento.getDate(),
                desligamento.getMotivo()});
        }
    }//GEN-LAST:event_btn_pesActionPerformed

