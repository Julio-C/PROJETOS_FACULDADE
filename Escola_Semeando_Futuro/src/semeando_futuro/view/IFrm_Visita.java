    private void btn_pesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesActionPerformed
        VisitaDAO ldao = new VisitaDAO();
        Visita l = new Visita();
        String campo=null;
        if(cb_pes.getSelectedIndex()==0){
            campo="Nome_Mae";
        }else if(cb_pes.getSelectedIndex()==1){
            campo="Nome_Pai";
        }else if(cb_pes.getSelectedIndex()==2){
            campo="Nome_Aluno";
        }else if(cb_pes.getSelectedIndex()==3){
            campo="Data_Visita";
        }
        

        ldao.Consultar(campo, txt_pes.getText(), l);
             
                lbl_id.setText(l.getIdVisita()+"");
               txt_nomemaecons.setText(l.getNomeMae()+"");
                txt_nomepaicons.setText(l.getNomePai()+"");
                txt_nomefilhocons.setText(l.getNomeFilho()+"");
                txt_telcons.setText(l.getTelefone()+"");
                txt_celcons.setText(l.getCelular());
                txt_datacans.setDate(l.getDate());
                txt_emailcons.setText(l.getEmail());
        
        
        

        List<Visita> visitas = ldao.getLista(campo, txt_pes.getText());
        DefaultTableModel model = (DefaultTableModel) tbl_visita.getModel();
        model.setNumRows(0);

        for (Visita visita : visitas) {
            model.addRow(new Object[]{
                visita.getIdVisita(),
                visita.getNomeMae(),
                visita.getNomePai(),
                visita.getNomeFilho(),
                visita.getTelefone(),
                visita.getCelular(),
                visita.getEmail(),
                visita.getDate()});
        }
    }//GEN-LAST:event_btn_pesActionPerformed

