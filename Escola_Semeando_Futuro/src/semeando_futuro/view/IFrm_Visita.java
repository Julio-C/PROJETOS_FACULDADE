    private void btn_cadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadActionPerformed
        Visita a = new Visita();

        if (txt_nomemaecad.getText().equals("") && txt_nomepaicad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o nome de algum responsável!");
        } else if (txt_nomefilhocad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o nome do Filho(a)");
        }else if (txt_telcad.getText().equals("")&&txt_celcad.equals("")&&txt_emailcad.equals("")) {
            JOptionPane.showMessageDialog(null, "Insira algum meio de contato!");
        }else if (txt_datacad.getDate()==null) {
            JOptionPane.showMessageDialog(null, "Insira a Data da Visita!");
        } else {
            try {
                java.util.Date F = txt_datacad.getDate();
                SimpleDateFormat format1 = new SimpleDateFormat("yyyy/MM/dd");
                String datavisita = format1.format(F);

                a.setNomeMae(txt_nomemaecad.getText());
                a.setNomePai(txt_nomepaicad.getText());
                a.setNomeFilho(txt_nomefilhocad.getText());
                a.setTelefone(txt_telcad.getText());
                a.setCelular(txt_celcad.getText());
                a.setDataVisita(datavisita);
                a.setEmail(txt_emailcad.getText());
                
                
            } catch (Exception e) {

            }
            VisitaDAO adao = new VisitaDAO();
            adao.adciona(a);
            JOptionPane.showMessageDialog(null, "Visita Cadastrada com sucesso");
        }
    }//GEN-LAST:event_btn_cadActionPerformed

