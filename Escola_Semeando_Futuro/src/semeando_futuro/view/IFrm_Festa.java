    private void btn_pesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesActionPerformed
        FestaDAO ldao = new FestaDAO();
        Festa l = new Festa();
        String campo=null;
        if(cb_pes.getSelectedIndex()==0){
            campo="Aluno.RA";
        }else if(cb_pes.getSelectedIndex()==1){
            campo="data_festa";
        }
        

        ldao.Consultar(campo, txt_pes.getText(), l);
             
    
        String r = l.getPagamento().toString();
        r.replace(".", "");
        
        lbl_id.setText(""+l.getId_Festa());
        txt_RAAlunoCons.setText(""+l.getRAAluno());
        txt_NomeCons.setText(""+l.getNome());
        txt_datacons.setDate(l.getDate());
        txt_obsCons.setText(""+l.getObservacao());
        txt_pagamentocons.setText(r);

        List<Festa> festas = ldao.getLista(campo, txt_pes.getText());
        DefaultTableModel model = (DefaultTableModel) tbl_festa.getModel();
        model.setNumRows(0);

        for (Festa festa : festas) {
            model.addRow(new Object[]{
                festa.getId_Festa(),
                festa.getRAAluno(),
                festa.getNome(),
                festa.getDate(),
                festa.getPagamento(),
                festa.getObservacao()});
        }
    }//GEN-LAST:event_btn_pesActionPerformed

