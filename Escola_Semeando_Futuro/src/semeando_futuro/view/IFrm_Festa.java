    private void btn_cadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadActionPerformed
        
        Festa a = new Festa();

        if (txt_DataCad.getDate()==null) {
            JOptionPane.showMessageDialog(null, "Selecione a Data Corretamente");
        } else if (txt_NomeCad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o nome da festa Corretamente");
        }else if (txt_pagamentoCad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o valor Corretamente");
        } else {
            try {
                java.util.Date F = txt_DataCad.getDate();
                SimpleDateFormat format1 = new SimpleDateFormat("yyyy/MM/dd");
                String datachamada = format1.format(F);

                a.setRAAluno(txt_RAALUNOCAD.getText());
                a.setNome(txt_NomeCad.getText());
                a.setData_Festa(datachamada);
                a.setPagamento(Double.parseDouble(txt_pagamentoCad.getText().replace("R$", "").replace(",", ".")));
                a.setObservacao(txt_ObsCad.getText());
                
                
            } catch (Exception e) {

            }
            FestaDAO adao = new FestaDAO();
            adao.adciona(a);
            JOptionPane.showMessageDialog(null, "Festa Cadastrada com sucesso");
        }
    }//GEN-LAST:event_btn_cadActionPerformed

